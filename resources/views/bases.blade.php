<h3>ETAPAS DEL CONCURSO</h3>
<p><b>Primera etapa:</b> Los Clientes de ENGIE deberán estar plenamente registrados para poder ser parte del presente concurso y no contar con adeudos en su cuenta.</p>
<p><b>Segunda etapa:</b> Contestar la trivia que aparecerá en la página de Internet www.engietepremia.com.mx a partir del 03 de junio y hasta el 28 de junio de 2019, en un horario de 08:00 a 23:59 horas., de no hacerlo no se tendrá por concluida la etapa y el interesado no formará parte del concurso.</p>
<p><b>Tercera etapa:</b> Un jurado calificador revisará si cada uno de los concursantes cumplió con los requisitos, etapas y condiciones para poder otorgarles su premio.</p>
<p><b>JURADO CALIFICADOR:</b> La determinación del ganador al premio ofrecido por parte del Jurado Calificador será inapelable y predominará en todo momento la fuente de información tomada por el organizador y por el cumplimiento de las bases, etapas y condiciones de participación. El Premio se asignará al momento en que el participante haya completado las etapas y el jurado determine conforme a los tiempos de contestación y certeza a las respuestas de la TRIVIA, así como de la zona de participación.</p>
<p>Esta etapa será avalada por un Fedatario Público. El jurado estará integrado por 2 (DOS) personas de ENGIE: (i) Vicepresidente de Asuntos Corporativos o Gerente de Mercadotecnia y Relaciones Públicas, (ii) Contralor o Personal de Auditoría Interna, y (iii) Director de Jurídico o Representante Legal y 1 (UNA) persona de la empresa Co-Workers, responsable del Concurso.</p>
