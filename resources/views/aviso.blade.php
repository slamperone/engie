S.A. de C.V., Tractebel GNP, de C.V. y Tamauligas, S.A. de C.V., en adelante referidas como "ENGIE", será
responsable, de manera separada y no solidaria, del tratamiento de los datos personales de sus clientes, sus
prospectos de clientes y usuarios.</p>

<p>Para efectos del presente aviso las empresas de “ENGIE” señalan de manera conjunta como domicilio para oír y
recibir notificaciones el ubicado en: Blvd. Manuel Ávila Camacho #36, Piso 16, Col. Lomas de Chapultepec, Del.
