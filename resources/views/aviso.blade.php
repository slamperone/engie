directa o indirectamente (de manera mayoritaria o minoritaria), sea ENGIE S.A., sociedad organizada bajo las
leyes de Francia incluyendo, sin limitación: ENGIE México S.A. de C.V., ENGIE México Servicios, ENGIE Factory
México S.A. de C.V., Denda México S.A. de C.V., [GDF Suez México Comercializadora, S. de R.L. de C.V., Servicios
