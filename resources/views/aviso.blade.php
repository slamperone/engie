<p>“ENGIE” en cumplimiento de la Ley ha desarrollado las políticas y lineamientos internos para la protección de los
datos personales de sus clientes, prospectos de clientes y usuarios. Este aviso especifica los derechos con los
que usted cuenta conforme a lo previsto por la Ley y el uso o tratamiento de dichos Datos Personales.</p>

<h2>Contexto, Problemas y Desafíos</h2>

<p>El Grupo está profundamente comprometido con la protección de los Datos Personales y la privacidad personal,
los cuales son valores establecidos en su Carta de Ética.</p>

<p>ENGIE procesa Datos Personales relacionados con sus empleados, clientes, socios, proveedores de servicios y
proveedores en el curso de sus actividades diarias (gestión de personal, gestión de prospectos y soluciones de
clientes, etc.).</p>

<p>Las personas son cada vez más conscientes de los datos que comparten y esperan un tratamiento y protección
adecuados de sus Datos personales.</p>

<p>Las autoridades públicas son cada vez más conscientes de estos asuntos. Están imponiendo obligaciones más
estrictas a las empresas que procesan datos personales y pueden aplicarlas mediante sanciones civiles, penales y
financieras. Por lo tanto, el Grupo y sus Entidades deben cumplir con estas regulaciones2.</p>

<p>En consecuencia, el Grupo se encuentra cada vez más expuesto a los riesgos asociados con la recopilación, el
uso, la modificación, el compromiso e incluso la falsificación de datos personales internos o externos inadecuados.</p>

<p>En función de sus valores éticos sobre datos personales y privacidad, y muy consciente de la importancia de las
reglas de protección de datos y privacidad y los riesgos que se corren en caso de infracciones de datos, el Grupo
se compromete a proteger dichos datos y privacidad, y en consecuencia está desplegando esta política.</p>


<h2>Finalidad del tratamiento de sus datos personales</h2>

<p>Sus datos personales serán utilizados para las siguientes finalidades:</p>


<ul>
	<li>Proveer los servicios, soluciones sustentables, aplicaciones digitales y productos requeridos por el cliente,
usuario o cliente prospectivo.</li>
	<li>Informar sobre cambios o nuevos productos o servicios que estén relacionados con el contratado o
adquirido por el cliente.</li>
	<li>Dar cumplimiento a obligaciones contraídas con nuestros clientes.</li>
	<li>Mercadotecnia, publicidad o prospección comercial.</li>
	<li>Atender los reportes de emergencia recibidos.</li>
	<li>Evaluar la calidad del servicio.</li>
	<li>Notificar al Buró de Crédito respecto al plazo para el cumplimiento de obligaciones de nuestros clientes y
verificar el historial crediticio de los prospectos de clientes que así lo autoricen.</li>
	<li>Cumplir con las obligaciones regulatorias, preparar los reportes requeridos por las autoridades
competentes.</li>
	<li>Realizar estudios internos sobre hábitos de consumo, promociones comerciales y servicios en materia
energética requeridos por nuestros clientes.</li>
	<li>Enviar avisos, mensajes, invitaciones, así como publicidad y publicaciones de forma impresa o a través
de medios electrónicos, telefónicos, físicos y presenciales, a menos que usted como Titular manifieste
expresamente su voluntad de no recibir este tipo de información.</li>
</ul>


<p>ENGIE implementará las medidas de seguridad, técnicas, administrativas y físicas, necesarias para proteger sus
datos personales, así como en el uso, acceso o tratamiento no autorizado. ENGIE únicamente utilizará sus datos
personales para los fines establecidos en el presente Aviso de Privacidad.</p>


<h2>Obtención y origen de datos personales</h2>

<p>Para las finalidades señaladas en el presente aviso de privacidad, podemos recabar sus datos personales de
distintas formas: cuando usted nos los proporciona directamente; cuando visita nuestro sitio de Internet; a
través de nuestros servicios de atención telefónica o utiliza nuestros servicios en línea, y cuando obtenemos
información a través de otras fuentes que están permitidas por la ley.</p>
<p>Los datos que obtenemos pueden ser:</p>


<ul>
	<li>Nombre, apellido, domicilio, números telefónicos fijos o celulares, correos electrónicos, clave del Registro
Federal de Contribuyentes, número de tarjeta de crédito, débito o cuenta bancaria para el servicio de cargo
recurrente, pago con tarjeta en agencia y/o vía telefónica.</li>
	<li>Aquellos datos propios y relacionados con los servicios que presta ENGIE en el cumplimiento de sus
obligaciones de distribución de gas natural, incluyendo la conexión, desconexión y lectura de consumos,
historial de pagos, adquisición de equipos o servicios adicionales.</li>
	<li>Su tipo de navegador, sistema operativo, la dirección IP y “cookies” almacenados en el sistema.</li>
</ul>


